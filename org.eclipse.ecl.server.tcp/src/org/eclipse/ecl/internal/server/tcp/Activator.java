package org.eclipse.ecl.internal.server.tcp;

import org.eclipse.core.runtime.Plugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends Plugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "org.eclipse.ecl.server.tcp";

	// The shared instance
	private static Activator plugin;

	@Deprecated
	private TcpServer server;

	/**
	 * The constructor
	 */
	public Activator() {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.runtime.Plugins#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.core.runtime.Plugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		if (server != null)
			server.interrupt();
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 * 
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	/**
	 * @deprecated Use {@link org.eclipse.ecl.server.tcp.EclTcpServerManager} to
	 *             manage servers
	 */
	@Deprecated
	public TcpServer getServer() {
		return server;
	}

	/**
	 * @deprecated Use {@link org.eclipse.ecl.server.tcp.EclTcpServerManager} to
	 *             manage servers
	 */
	@Deprecated
	public void setServer(TcpServer tcpServer) {
		this.server = tcpServer;
	}
}
